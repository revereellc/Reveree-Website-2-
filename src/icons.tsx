// Raw inner SVG for spots that render the icon without the .icon-box wrapper
// (the logo mark and footer logo in the original used REVEREE_ICONS["location-pin"]
// directly). Exposed so those usages match exactly.
export function ReveereRawIcon({ name }: { name: string }): ReactElement {
  return REVEREE_ICONS[name] ?? REVEREE_ICONS.sparkle;
}

/**
 * Returns an inline SVG icon wrapped in a styled box.
 * size: "sm" (32px), "md" (48px), "lg" (72px)
 */
export function ReveereIcon({
  name,
  size = "md",
}: {
  name: string;
  size?: IconSize;
}): ReactElement {
  const svg = REVEREE_ICONS[name] ?? REVEREE_ICONS.sparkle;
  return <span className={`icon-box icon-${size}`}>{svg}</span>;
}
